# ── KafkaTransactionEvent ─────────────────────────────────────────────────────


class TestKafkaTransactionEvent:
    def test_envelope_wraps_transaction(self):
        tx = Transaction(
            timestamp=datetime.now(UTC),
            sender_account_id="acc-001",
            amount_zar=Decimal("500"),
            payment_rail=PaymentRail.EFT,
        )
        event = KafkaTransactionEvent(
            produced_at=datetime.now(UTC),
            partition_key=tx.sender_account_id,
            payload=tx,
        )
        assert event.event_type == "transaction.created"
        assert event.schema_version == "1.0"
        assert event.payload.sender_account_id == "acc-001"

    def test_event_id_autogenerated(self):
        tx = Transaction(
            timestamp=datetime.now(UTC),
            sender_account_id="acc-001",
            amount_zar=Decimal("100"),
            payment_rail=PaymentRail.MOBILE_APP,
        )
        event = KafkaTransactionEvent(
            produced_at=datetime.now(UTC),
            partition_key="acc-001",
            payload=tx,
        )
        assert len(event.event_id) > 0
